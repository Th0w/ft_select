int			ft_iswhitespace(char c)
{
	return (c == ' ' || c == '\n' || c == '\t');
}

int			ft_isnum(char c)
{
	return (c >= '0' && c <= '9');
}

int			ft_isnumeric(char c)
{
	return (ft_isnum(c) || c == '-' || c == '+');
}
